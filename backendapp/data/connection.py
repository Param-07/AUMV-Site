import psycopg
from psycopg.rows import dict_row
from config import Config

DATABASE_URL = Config.DATABASE_URL

def get_conn():
    return psycopg.connect(
        DATABASE_URL,
        row_factory=dict_row,   
        autocommit=True        
    )

def put_conn(conn):
    try:
        conn.close()
    except Exception:
        pass
